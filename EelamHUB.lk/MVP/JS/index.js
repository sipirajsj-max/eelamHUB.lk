/* ==========================================================================
   FUNCTION 1: LANGUAGE SWITCHER LOGIC
   ========================================================================== */
function changeLanguage(lang) {
    // 1. ஆக்டிவ் பட்டன் ஸ்டைலை மாற்றுதல்
    const buttons = document.querySelectorAll('.lang_btn');
    buttons.forEach(btn => btn.classList.remove('active'));
    
    const activeBtn = document.getElementById(`btn-${lang}`);
    if (activeBtn) activeBtn.classList.add('active');

    // 2. HTML உரைப்பகுதிகளை மாற்றுதல் (ID-களை அடிப்படையாகக் கொண்டு)
    const langData = translations[lang];
    
    for (let id in langData) {
        const element = document.getElementById(id);
        if (element) {
            // HTML tags (<br>, <span>) வேலை செய்ய innerHTML பயன்படுத்தப்படுகிறது
            element.innerHTML = langData[id];
        }
    }

    // 3. Input Placeholders மற்றும் Select Box உரைகளை மாற்றுதல்
    const keywordInput = document.querySelector('.search_form input[type="text"]');
    if (keywordInput) keywordInput.placeholder = langData["search-keyword"];

    const regionSelect = document.querySelector('.search_form select');
    if (regionSelect && regionSelect.options[0]) {
        regionSelect.options[0].text = langData["search-region"];
    }

    const searchBtn = document.querySelector('.search_btn');
    if (searchBtn) searchBtn.innerText = langData["search-btn"];

    // 4. மாவட்ட அட்டவணையில் உள்ள "Active Jobs" உரையை மாற்றுதல்
    const jobCounts = document.querySelectorAll('.job_count');
    jobCounts.forEach(count => {
        const currentNum = count.innerText.match(/\d+\+/); // எண்களை மட்டும் பிரித்தெடுக்க (எ.கா: 450+)
        if (currentNum) {
            count.innerText = `${currentNum[0]} ${langData["job-suffix"]}`;
        }
    });

    // 5. SEO பயன்பாட்டிற்காக html லேங்குவேஜ் டேக்-ஐ மாற்றுதல்
    document.documentElement.lang = lang;

    // லோக்கல் மெமரியில் சேமித்தல்
    localStorage.setItem('preferredLang', lang);
}

/* ==========================================================================
   FUNCTION 2: MOBILE HAMBURGER MENU (3-LINE) TOGGLE
   ========================================================================== */
function toggleMobileMenu() {
    const langBar = document.getElementById("langBar");
    const menuToggle = document.querySelector(".mobile_menu_toggle");
    
    if (langBar) {
        langBar.classList.toggle("show");
        
        // பட்டன் ஐகானை மாற்றுதல் (☰ லிருந்து X ஆக)
        if (langBar.classList.contains("show")) {
            menuToggle.innerText = "✕";
        } else {
            menuToggle.innerText = "☰";
        }
    }
}

// மொபைல் மெனு திறந்திருக்கும் போது வெளியில் அழுத்தினால் மெனு தானாக மூடப்பட
document.addEventListener('click', function(event) {
    const langBar = document.getElementById("langBar");
    const menuToggle = document.querySelector(".mobile_menu_toggle");
    
    if (langBar && langBar.classList.contains('show')) {
        if (!langBar.contains(event.target) && !menuToggle.contains(event.target)) {
            langBar.classList.remove('show');
            menuToggle.innerText = "☰";
        }
    }
});

